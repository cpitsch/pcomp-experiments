import pickle
from argparse import ArgumentParser
from dataclasses import dataclass
from pathlib import Path
from timeit import default_timer
from typing import Any, Literal

import pandas as pd
from mpire.pool import WorkerPool
from pcomp.binning import KMeans_Binner
from pcomp.emd.comparators.permutation_test import (
    Timed_Levenshtein_PermutationComparator,
)
from pcomp.utils import import_log

parser = ArgumentParser()


parser.add_argument(
    "-s", "--seed", type=int, help="The seed to run the experiments for.", required=True
)
parser.add_argument(
    "-c",
    "--cores",
    type=int,
    help="The number of cores to use for multiprocessing.",
    required=True,
)
args = parser.parse_args()


LOGS_BASE_PATH = Path("road_traffic_synthetic", str(args.seed))

if not LOGS_BASE_PATH.exists():
    raise ValueError(
        "The event log path does not exist. Please make sure you supplied a valid seed and that the event logs are present."
    )

OUTPUT_BASE_PATH = Path("road_traffic_synthetic_results_WEIGHTED_TIME", str(args.seed))
DIST_SIZE = 10_000
SEED = 1337
WEIGHTED_TIME_COST = True
SIGNIFICANCE_LEVEL = 0.05


@dataclass
class EventLogSetting:
    # True iff the log was generated to have no difference (i.e., std=0, p=0 does not count)
    # These are event logs that are generated by randomly splitting an event log.
    is_no_diff_log: bool
    base_path: Path
    probability: float
    std_shift: float
    identifier: str
    seed: int

    def __init__(self, base_path: Path, is_no_diff_log: bool = False) -> None:
        self.is_no_diff_log = is_no_diff_log
        self.base_path = base_path
        assert all(path.exists() for path in self.get_log_paths())

        if self.is_no_diff_log:
            self.probability = 0
            self.std_shift = 0
            raise NotImplementedError("TODO: Implement no_diff logs")
        else:
            # Path: road_traffic_synthetic/<seed>/ServiceTimeStdShifter_Send Fine_std<std>_p<p>_seed_<seed>/
            assert base_path.name.startswith("ServiceTimeStdShifter")
            # ServiceTimeStdShifter, Send Fine, std<std>, p<p>, seed, <seed>
            splitted = base_path.name.split("_")
            std_part = splitted[2]
            p_part = splitted[3]
            seed = int(splitted[5])
            assert std_part.startswith("std")
            assert p_part.startswith("p")
            self.std_shift = float(std_part[3:])
            self.probability = float(p_part[1:])
            self.identifier = "_".join([std_part, p_part])
            self.seed = seed

    def get_log_paths(self) -> tuple[Path, Path]:
        if not self.is_no_diff_log:
            return (
                self.base_path / ".." / "log_1.xes.gz",
                self.base_path / "log_2.xes.gz",
            )
        else:
            raise NotImplementedError("TODO: Implement no_diff logs")

    def has_drift(self) -> bool:
        return self.probability > 0 and self.std_shift > 0


def get_change_log_base_paths(path: Path) -> list[Path]:
    return [p.parent for p in path.rglob("*log_2.xes.gz")]


def get_change_log_settings(path: Path) -> list[EventLogSetting]:
    return [
        EventLogSetting(base_path, False)
        for base_path in get_change_log_base_paths(path)
    ]


@dataclass
class Instance:
    log: EventLogSetting

    weighted_time_cost: bool

    @property
    def path(self) -> Path:
        return (
            OUTPUT_BASE_PATH
            / ("weighted_time" if self.weighted_time_cost else "normal_time")
            / self.log.identifier
        )

    @property
    def pickle_path(self) -> Path:
        return self.path / "result.pkl"

    @property
    def technique_name(self) -> str:
        return "permutation_test"

    def get_logs(self) -> tuple[pd.DataFrame, pd.DataFrame]:
        log_1_path, log_2_path = self.log.get_log_paths()
        return (
            import_log(log_1_path.as_posix(), show_progress_bar=False),
            import_log(log_2_path.as_posix(), show_progress_bar=False),
        )

    def get_comparator(
        self, verbose: bool = True
    ) -> Timed_Levenshtein_PermutationComparator:
        return Timed_Levenshtein_PermutationComparator(
            *self.get_logs(),
            weighted_time_cost=self.weighted_time_cost,
            binner_factory=KMeans_Binner,
            binner_args={"k": 3},
            distribution_size=DIST_SIZE,
            seed=SEED,
            verbose=verbose,
        )

    def load_pickle(self) -> Timed_Levenshtein_PermutationComparator:
        if not self.pickle_path.exists():
            raise ValueError(
                "Attempt to load non-existent pickle file:", self.pickle_path.as_posix()
            )
        with open(self.pickle_path, "rb") as f:
            return pickle.load(f)

    def to_result_row(
        self, pval: float, logs_emd: float, duration_seconds: float
    ) -> dict[str, Any]:
        detection = pval < SIGNIFICANCE_LEVEL
        log_has_drift = self.log.has_drift()
        is_correct = detection == log_has_drift

        return {
            "technique": "Permutation Test",
            "mutation_probability": self.log.probability,
            "mutation_std_shift": self.log.std_shift,
            "log_path": self.log.base_path.as_posix(),
            "binner": "kmeans_3",
            "weighted_time_cost": self.weighted_time_cost,
            "has_diff": log_has_drift,
            "is_no_diff_log": self.log.is_no_diff_log,
            "pval": pval,
            "logs_emd": logs_emd,
            "detection": detection,
            "correct": is_correct,
            "classification_class": get_classification_class(detection, log_has_drift),
            "duration": duration_seconds,
        }

    def run_and_save_results(self, verbose: bool = True) -> dict[str, Any]:
        start_time = default_timer()
        comparator = self.get_comparator(verbose)
        res = comparator.compare()
        end_time = default_timer()

        # Save results
        self.path.mkdir(parents=True)
        with open(self.pickle_path, "wb") as f:
            pickle.dump(res, f)

        return self.to_result_row(res.pvalue, res.logs_emd, end_time - start_time)


def get_classification_class(
    detection: bool, has_diff: bool
) -> Literal["TP", "TN", "FP", "FN"]:
    is_correct = detection == has_diff
    is_positive = detection

    first_part = "T" if is_correct else "F"
    second_part = "P" if is_positive else "N"
    return first_part + second_part


def run_instance(instance: Instance) -> dict[str, Any]:
    return instance.run_and_save_results(verbose=True)


def main():
    start_time = default_timer()
    logs = get_change_log_settings(LOGS_BASE_PATH)
    instances = [Instance(log_setting, WEIGHTED_TIME_COST) for log_setting in logs]
    assert all(instance.weighted_time_cost for instance in instances)
    assert not any(instance.pickle_path.exists() for instance in instances)
    # instances = [
    #    instance for instance in instances if not instance.pickle_path.exists()
    # ]

    with WorkerPool(args.cores) as p:
        results = p.map(run_instance, instances, progress_bar=True)
    df = pd.DataFrame(results)
    df.to_csv(OUTPUT_BASE_PATH / "summary.csv", index=False)

    print(f"Elapsed Time: {default_timer() - start_time}")


if __name__ == "__main__":
    main()
